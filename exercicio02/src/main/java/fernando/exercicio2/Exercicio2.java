public class Exercicio2 {

    public static void main(String[] args) {
        // adição 13 + 75:
        final int NUMERO1 = 13;
        int numero1 = 75;
        int resultado1 = NUMERO1 + numero1;
        System.out.println("resultado de 13 + 75 = " + resultado1);
        
        // subtração 75 - 22:
        final int NUMERO2 = 75;
        int numero2 = 22;
        int resultado2 = NUMERO2 - numero2;
        System.out.println("resultado de 75 - 22 = " + resultado2);
        
        // multiplicação 1974 * 13:
        final int NUMERO3 = 1974;
        int numero3 = 13;
        int resultado3 = NUMERO3 * numero3;
        System.out.println("resultado de 1974 * 13 = " + resultado3);
        
        // divisão 75 / 22:
        final int NUMERO4 = 75;
        int numero4 = 22;
        int resultado4 = NUMERO4 / numero4;
        System.out.println("resultado de 75 / 22 = " + resultado4);
        
        // resto da divisão 75 / 22
        int resultado5 = NUMERO4 % numero4;
        System.out.println("resultado de 75 % 22 = " + resultado5);
        
        
    }
}
